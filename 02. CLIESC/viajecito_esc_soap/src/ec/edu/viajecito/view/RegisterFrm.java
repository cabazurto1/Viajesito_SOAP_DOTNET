    private void txtTelfKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelfKeyTyped
        char c = evt.getKeyChar();

    // Solo permitir dígitos
    if (!Character.isDigit(c)) {
        evt.consume(); // Bloquea el carácter si no es número
    }

    // Limitar a 10 caracteres
    if (txtTelf.getText().length() >= 10) {
        evt.consume(); // Bloquea si ya hay 10 caracteres
    }
    }//GEN-LAST:event_txtTelfKeyTyped

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String nombre = txtName.getText().trim();
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());
        String passwordRep = new String(txtPasswordRepit.getPassword());
        String telefono = txtTelf.getText().trim();

        // Validaciones básicas
        if (nombre.isEmpty() || username.isEmpty() || password.isEmpty() || passwordRep.isEmpty() || telefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!password.equals(passwordRep)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden.", "Error de contraseña", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!telefono.matches("\\d{1,10}")) {
            JOptionPane.showMessageDialog(this, "El teléfono debe contener solo números y tener como máximo 10 dígitos.", "Teléfono inválido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Crear el objeto Usuario
        Usuario usuario = new Usuario();
        usuario.setNombre(nombre);
        usuario.setUsername(username);
        usuario.setPassword(password);
        usuario.setTelefono(telefono);

        // Llamar al controlador para registrar
        UsuariosController controller = new UsuariosController();
        boolean exito = controller.crearUsuario(usuario);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente.", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
            // Aquí podrías limpiar los campos o cerrar la ventana
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el usuario. Intente nuevamente.", "Error de registro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

