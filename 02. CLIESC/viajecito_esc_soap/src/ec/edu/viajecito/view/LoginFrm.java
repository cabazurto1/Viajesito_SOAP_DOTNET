    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword()); // Si usas JPasswordField

        if (username.isEmpty() || password.isEmpty()) {
            return;
        }

        UsuariosController controller = new UsuariosController();
        Usuario usuario = controller.login(username, password);

        if (usuario != null) {
            new MenuFrm(usuario).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Credenciales inválidas.", "Error de autenticación", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

