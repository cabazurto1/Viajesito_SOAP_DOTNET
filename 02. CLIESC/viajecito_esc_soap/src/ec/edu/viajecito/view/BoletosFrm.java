    private void cargarTablaBoletos() {
        // Columnas para la tabla
        String[] columnas = {"N° Boleto", "Vuelo", "Fecha de Compra", "Precio"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);

        // Asegurarse de que haya un usuario
        if (usuario == null) {
            JOptionPane.showMessageDialog(this, "No hay un usuario seleccionado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtener los boletos del usuario
        List<Boleto> boletos = new BoletosController().obtenerBoletosPorUsuario(usuario.getIdUsuario().toString());

        for (Boleto b : boletos) {
            Object[] fila = new Object[]{
                b.getNumeroBoleto(),
                b.getVuelo().getCodigoVuelo(),
                b.getFechaCompra(),  // Puedes aplicar formato si es necesario
                b.getPrecioCompra()
            };
            modelo.addRow(fila);
        }

        tblBolotos.setModel(modelo);
    }


    private void tableStyle() {
        // Cambiar fuente
        tblBolotos.setFont(new Font("Maiandra GD", Font.PLAIN, 14));

        // Cambiar alto de las filas
        tblBolotos.setRowHeight(25);

        // Cambiar color de fondo y texto de las filas
        tblBolotos.setBackground(Color.WHITE);
        tblBolotos.setForeground(Color.BLACK);
        
        tblBolotos.setFillsViewportHeight(true);

        // Cambiar colores de encabezado
        JTableHeader header = tblBolotos.getTableHeader();        
        header.setDefaultRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected,
                                                           boolean hasFocus, int row, int column) {
                JLabel label = new JLabel(value.toString());
                label.setOpaque(true);
                label.setFont(new Font("Maiandra GD", Font.BOLD, 14));
                label.setBackground(new Color(60,59,46));
                label.setForeground(Color.WHITE);  
                label.setBorder(BorderFactory.createLineBorder(new Color(230, 230, 230)));
                label.setHorizontalAlignment(SwingConstants.CENTER);
                return label;
            }
        });      

        // Bordes
        tblBolotos.setShowHorizontalLines(true);
        tblBolotos.setShowVerticalLines(true);
        tblBolotos.setGridColor(new Color(230, 230, 230)); // Líneas suaves
        
        tblBolotos.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {

                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                if (isSelected) {
                    c.setBackground(new Color(144, 202, 249)); // azul claro para selección
                    c.setForeground(Color.BLACK);
                } else {
                    c.setBackground(row % 2 == 0 ? Color.WHITE : new Color(255, 241, 191)); // filas alternas
                    c.setForeground(Color.BLACK);
                }

                return c;
            }
        });
    }

