    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        if (vuelos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vuelo no seleccionado.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Vuelo vuelo = vuelos.get(cmbVuelos.getSelectedIndex());
        int numBoletos = (int) spnNumBoletos.getValue();
        double precio = vuelo.getValor().doubleValue() * numBoletos;
        
        if (numBoletos < 1) {
            JOptionPane.showMessageDialog(this, "Indique la cantidad de boletos a comprar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (numBoletos > vuelo.getDisponibles()) {
            JOptionPane.showMessageDialog(this, "La cantidad de boletos eccede los Disponibles.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        CompraBoletoRequest compraBoletoRequest = new CompraBoletoRequest(vuelo.getIdVuelo(), usuario.getIdUsuario(), numBoletos);                
        
        Object[] opciones = {"Confirmar compra", "Cancelar"};

        int opcion = JOptionPane.showOptionDialog(
            this,
            "Â¿EstÃ¡ seguro que desea comprar el boleto?\n"
                + "Vuelo: " + vuelo.getCodigoVuelo() + "\n"
                + "Viaje: De " + vuelo.getCiudadOrigen().getNombreCiudad() + " a " + vuelo.getCiudadDestino().getNombreCiudad() + "\n"
                + "Fecha: " + vuelo.getHoraSalida() + "\n"
                + "NÃºmero de Boletos: " + numBoletos + "\n"
                + "Precio Total: " + precio + "$\n",
            "ConfirmaciÃ³n",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,
            null,
            opciones,
            opciones[0]
        );

        if (opcion == JOptionPane.YES_OPTION) {
            // AcciÃ³n de compra
            System.out.println("Compra confirmada.");
            boolean wasBuy = new BoletosController().comprarBoletos(compraBoletoRequest);
            
            if (wasBuy) {
                JOptionPane.showMessageDialog(this, "Compra realizada con Ã©xito.", "Ãxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Hubo problemas al comprar el boleto intente mas tarde.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            
            
            
        }
    }//GEN-LAST:event_btnComprarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String origen = ciudades.get(cmbOrigen.getSelectedIndex()).getCodigoCiudad();
        String destino = ciudades.get(cmbDestino.getSelectedIndex()).getCodigoCiudad();
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String horaSalida = sdf.format((Date) spinDate.getValue());
        
        vuelos = new VuelosController().obtenerVuelosPorCiudad(origen, destino, horaSalida);
        cmbVuelos.removeAllItems();

        if (vuelos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vuelo no Disponible.", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
        } else {
            for (Vuelo v : vuelos) {
                cmbVuelos.addItem(v.getCodigoVuelo() + " - Precio: " + v.getValor() + "$ - Disponibles: " + v.getDisponibles());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

